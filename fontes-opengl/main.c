GLuint fontOffset;

void criaFonteMatricial(void)
{
    GLuint i, j;
    glPixelStorei(GL_UNPACK_ALIGNMENT, 1);

    fontOffset = glGenLists(128);
    for (i = 0, j = 'A'; i < 26; i++, j++) {
        glNewList(fontOffset + j, GL_COMPILE);
            glBitmap(8, 13, 0.0, 2.0, 10.0, 0.0, letras[i]);
        glEndList();
    }
    glNewList(fontOffset + ' ', GL_COMPILE);
        glBitmap(8, 13, 0.0, 2.0, 10.0, 0.0, espaco);
    glEndList();
}

void init(void)
{
    criaFonteMatricial();
}

void escreveTexto(char *s)
{
    glPushAttrib(GL_LIST_BIT);
    glListBase(fontOffset);
    glCallLists(strlen(s), GL_UNSIGNED_BYTE, (GLubyte*) s);
    glPopAttrib();
}

void desenha(void)
{
    GLfloat corBranca[3] = { 1, 1, 1 };

    glClear(GL_COLOR_BUFFER_BIT);
    glColor3fv(corBranca);

    glRasterPos2i(20, 60);
    escreveTexto("THE QUICK BROWN FOX JUMPS");
    glRasterPos2i(20, 40);
    escreveTexto("OVER A LAZY DOG");
    glFlush();
}

void redimensiona(int w, int h)
{
    glViewport(0, 0, (GLsizei) w, (GLsizei) h);
    glMatrixMode(GL_PROJECTION);
    glLoadIdentity();
    glOrtho (0.0, w, 0.0, h, -1.0, 1.0);
    glMatrixMode(GL_MODELVIEW);
}

void teclado(unsigned char key, int x, int y)
{
    switch (key) {
        case 27:
            exit(0);
   }
}

int main(int argc, char** argv)
{
    glutInit(&argc, argv);
    glutInitDisplayMode(GLUT_SINGLE | GLUT_RGB);
    glutInitWindowSize(300, 100);
    glutInitWindowPosition (100, 100);
    glutCreateWindow(argv[0]);
    init();
    glutReshapeFunc(redimensiona);
    glutKeyboardFunc(teclado);
    glutDisplayFunc(desenha);
    glutMainLoop();
    return 0;
}
